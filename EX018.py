from math import radians, sin, cos, tan
an = float(input('Digite o angulo que você deseja: '))
s = sin(radians(an))
print('O SENO de {} é igual a {:.2f}'.format(an, s))
c = cos(radians(an))
print('O COSSENO de {} é igual a {:.2f}'.format(an, c))
t = tan(radians(an))
print('A TANGENTE de {} é igual a {:.2f}'.format(an, t))


